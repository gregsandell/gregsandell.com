    <p> 
SHARC is collection of datasets pertaining to some research in musical acoustics I did between 1989-1995.
The data consists of analyses of the <a href="http://www.music.mcgill.ca/resources/mums/html">McGill University Master Samples</a> recordings of individually performed orchestral instrument notes.  There are 32 instruments, and 1300 notes total.  I took a middle portion of each recording, during the note's steady state, analyzed its spectrum with a Fourier Transform, and SHARC is the result.  
